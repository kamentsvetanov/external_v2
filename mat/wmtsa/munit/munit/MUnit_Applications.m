% NAME
%   MUnit Applications
%
% DESCRIPTION
%   The MUnit test framework consists of several applications
%   to execute and manage the test cases and test suites.
%   The application functions are categorized as:
%
%   * Drivers   -- execute test cases and test suites.
%   * Utilities -- manage test cases and test suites.
%
% TOOLBOX
%    munit/munit
%
% CATEGORY
%
%
%***
%****m* Applications/Drivers
%
% NAME
%   MUnit Drivers -- MUnit applications for executing test cases and test suites.
%
% DESCRIPTION
%   The MUnit driver applications are functions to be run from the 
%   command shell to execute a test case or a test suite.
%
%   Driver applications include:
%     * run_tcase
%     * run_tsuite
% 
%***
%****m* Applications/Utilities
%
% NAME
%   MUnit Utilities -- MUnit applications analyzing and listing test cases and suites.
%
% DESCRIPTION
%   The MUnit utility applications are functions to be run from the 
%   command shell to analyze and list a test case or a test suite.
%
%   Utility applications include:
%     * list_tcase
%     * list_tsuite
%
